package defaulth;
// incremento e decremento
  
 public class incre_decre {  
   public static void main(String args[]) { 
	int a;  
       int b = 2;  
       int c;  
       a = b++;  
       c = ++b;  
       System.out.println("a = " + a);  
       System.out.println("b = " + b);  
       System.out.println("c = " + c);  
    }  
 }